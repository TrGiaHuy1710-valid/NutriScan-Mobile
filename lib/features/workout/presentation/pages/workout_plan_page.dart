            const SizedBox(height: 8),

            if (slots.isEmpty)
              const Center(
                child: Padding(
                  padding: EdgeInsets.symmetric(vertical: 32),
                  child: Text('Không có khung giờ rảnh nào được ghi nhận.', style: TextStyle(color: AppTheme.vibrantTextLight)),
                ),
              )
            else
              Column(
                children: slots.map((slot) {
                  return Card(
                    color: Colors.white,
                    margin: const EdgeInsets.only(bottom: 12),
                    child: Padding(
                      padding: const EdgeInsets.all(16),
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Row(
                            mainAxisAlignment: MainAxisAlignment.spaceBetween,
                            children: [
                              Text(
                                'Khung giờ: ${slot.startTime} - ${slot.endTime}',
                                style: const TextStyle(fontSize: 13, fontWeight: FontWeight.bold, color: AppTheme.vibrantSecondary),
                              ),
                              Row(
                                children: [
                                  Text(
                                    'Trống: ${slot.remainingDuration}p / ${slot.availableDuration}p',
                                    style: const TextStyle(fontSize: 11, color: AppTheme.vibrantTextMedium, fontWeight: FontWeight.bold),
                                  ),
                                  const SizedBox(width: 8),
                                  IconButton(
                                    icon: const Icon(Icons.delete_outline, color: AppTheme.vibrantAlertCoral, size: 18),
                                    onPressed: () {
                                      setState(() {
                                        _daySlotsMap[_selectedDay]?.removeWhere((s) => s.id == slot.id);
                                      });
                                    },
                                  )
                                ],
                              )
                            ],
                          ),
                          const Divider(),
                          if (slot.workouts.isEmpty)
                            Padding(
                              padding: const EdgeInsets.symmetric(vertical: 8),
                              child: Row(
                                mainAxisAlignment: MainAxisAlignment.spaceBetween,
                                children: [
                                  const Text('Chưa có lịch tập', style: TextStyle(fontSize: 11, color: AppTheme.vibrantTextLight)),
                                  TextButton.icon(
                                    onPressed: () => _showAddWorkoutToSlotDialog(slot),
                                    icon: const Icon(Icons.add, size: 14),
                                    label: const Text('Thêm bài', style: TextStyle(fontSize: 11)),
                                  )
                                ],
                              ),
                            )
                          else ...[
                            Column(
                              children: slot.workouts.map((w) {
                                return ListTile(
                                  contentPadding: EdgeInsets.zero,
                                  leading: Checkbox(
                                    value: w.isCompleted,
                                    activeColor: AppTheme.vibrantPrimary,
                                    onChanged: (val) {
                                      setState(() {
                                        w.isCompleted = val ?? false;
                                      });
                                      if (w.isCompleted) {
                                        widget.onWorkoutCompleted(
                                          WorkoutPlan(
                                            id: w.id,
                                            title: w.name,
                                            durationMinutes: w.durationMinutes,
                                            difficulty: 'Custom',
                                            equipment: 'None',
                                            estimatedCaloriesBurned: w.caloriesBurned,
                                            exercises: [w.name],
                                            tags: const [],
                                            isScheduled: true,
                                            isCompleted: true,
                                          ),
                                        );
                                      }
                                    },
                                  ),
                                  title: Text(
                                    w.name,
                                    style: TextStyle(
                                      fontSize: 12,
                                      fontWeight: FontWeight.bold,
                                      color: AppTheme.vibrantTextDark,
                                      decoration: w.isCompleted ? TextDecoration.lineThrough : null,
                                    ),
                                  ),
                                  subtitle: Text(
                                    '⏱ ${w.durationMinutes}p | 🔥 ${w.caloriesBurned} kcal ${w.isAutoGenerated ? "| 🤖 Auto" : ""}',
                                    style: const TextStyle(fontSize: 11, color: AppTheme.vibrantTextMedium),
                                  ),
                                  trailing: Row(
                                    mainAxisSize: MainAxisSize.min,
                                    children: [
                                      IconButton(
                                        icon: const Icon(Icons.edit_outlined, size: 16, color: AppTheme.vibrantPrimary),
                                        onPressed: () => _showEditWorkoutDialog(slot, w),
                                      ),
                                      IconButton(
                                        icon: const Icon(Icons.close, size: 16, color: AppTheme.vibrantAlertCoral),
                                        onPressed: () {
                                          setState(() {
                                            slot.workouts.removeWhere((item) => item.id == w.id);
                                          });
                                        },
                                      ),
                                    ],
                                  ),
                                );
                              }).toList(),
                            ),
                            const SizedBox(height: 8),
                            if (slot.remainingDuration > 0)
                              Align(
                                alignment: Alignment.centerRight,
                                child: TextButton.icon(
                                  onPressed: () => _showAddWorkoutToSlotDialog(slot),
                                  icon: const Icon(Icons.add, size: 14),
                                  label: const Text('Thêm bài tập', style: TextStyle(fontSize: 11)),
                                ),
                              ),
                          ]
                        ],
                      ),
                    ),
                  );
                }).toList(),
              ),
            const SizedBox(height: 32),
          ],
        ),
      ),
    );
  }
