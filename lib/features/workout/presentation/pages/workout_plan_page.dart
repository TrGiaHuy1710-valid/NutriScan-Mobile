  bool get _isGoalAchieved => _totalWorkoutTime >= _minRequiredTime;

  // Auto scheduler trigger
  void _triggerAutoSchedule() {
    final slots = _daySlotsMap[_selectedDay] ?? [];
    int currentTotal = _totalWorkoutTime;
    final goal = _minRequiredTime;

    int timeNeeded = goal - currentTotal;
    if (timeNeeded <= 0) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Chỉ tiêu đã hoàn thành xuất sắc! 🎉 ($currentTotal/$goal phút)')),
      );
      return;
    }

    final tempSlots = slots.map((s) => s.copyWith(workouts: List<ScheduledWorkoutItem>.from(s.workouts))).toList();
    bool isProgressMade = true;
    int itemsAdded = 0;
    int minutesAdded = 0;

    while (currentTotal < goal && isProgressMade) {
      isProgressMade = false;
      for (int i = 0; i < tempSlots.length; i++) {
        final slot = tempSlots[i];
        final remainingSpace = slot.remainingDuration;

        if (remainingSpace > 0) {
          final fitPresets = _presets.where((p) => (p['duration'] as int) <= remainingSpace).toList();
          if (fitPresets.isNotEmpty) {
            final selected = (fitPresets..shuffle()).first;
            final duration = selected['duration'] as int;
            final name = selected['name'] as String;
            final calories = selected['calories'] as int;

            final newItem = ScheduledWorkoutItem(
              id: 'auto_${DateTime.now().microsecondsSinceEpoch}',
              name: name,
              durationMinutes: duration,
              caloriesBurned: calories,
              isAutoGenerated: true,
            );

            tempSlots[i].workouts.add(newItem);
            currentTotal += duration;
            minutesAdded += duration;
            itemsAdded++;
            isProgressMade = true;

            // Trigger callback for SQLite tracking compatibility
            widget.onWorkoutScheduled(
              WorkoutPlan(
                id: newItem.id,
                title: name,
                durationMinutes: duration,
                difficulty: 'Auto',
                equipment: 'None',
                estimatedCaloriesBurned: calories,
                exercises: [name],
                tags: const [],
                isScheduled: true,
              ),
            );

            if (currentTotal >= goal) break;
          }
        }
      }
    }

    if (itemsAdded > 0) {
      setState(() {
        _daySlotsMap[_selectedDay] = tempSlots;
      });
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text('Đã xếp thêm $itemsAdded môn (+ $minutesAdded phút) vào khung giờ trống! 🤖⚡')),
      );
    } else {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Các khung giờ rảnh đều đã kín! Hãy tạo thêm khung rảnh mới. ⚠️')),
      );
    }
  }

