class ScheduledWorkoutItem {
  ScheduledWorkoutItem({
    required this.id,
    required this.name,
    required this.durationMinutes,
    required this.caloriesBurned,
    this.isCompleted = false,
    this.isAutoGenerated = false,
  });

  final String id;
  final String name;
  final int durationMinutes;
  final int caloriesBurned;
  bool isCompleted;
  final bool isAutoGenerated;

  ScheduledWorkoutItem copyWith({
    String? name,
    int? durationMinutes,
    int? caloriesBurned,
    bool? isCompleted,
  }) {
    return ScheduledWorkoutItem(
      id: id,
      name: name ?? this.name,
      durationMinutes: durationMinutes ?? this.durationMinutes,
      caloriesBurned: caloriesBurned ?? this.caloriesBurned,
      isCompleted: isCompleted ?? this.isCompleted,
      isAutoGenerated: isAutoGenerated,
    );
  }
}

class DayTimeSlot {
  DayTimeSlot({
    required this.id,
    required this.startTime,
    required this.endTime,
    required this.availableDuration,
    required this.workouts,
  });

  final String id;
  final String startTime;
  final String endTime;
  final int availableDuration;
  List<ScheduledWorkoutItem> workouts;

  int get usedDuration => workouts.fold(0, (sum, w) => sum + w.durationMinutes);
  int get remainingDuration => availableDuration - usedDuration;

  DayTimeSlot copyWith({
    List<ScheduledWorkoutItem>? workouts,
  }) {
    return DayTimeSlot(
      id: id,
      startTime: startTime,
      endTime: endTime,
      availableDuration: availableDuration,
      workouts: workouts ?? this.workouts,
    );
  }
}

