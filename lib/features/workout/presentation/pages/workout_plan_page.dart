                  final allowed = slot.availableDuration - otherWorkouts;

                  if (dur > allowed) {
                    ScaffoldMessenger.of(context).showSnackBar(
                      SnackBar(content: Text('Vượt quá giờ rảnh! (Cho phép tối đa ${allowed}p)')),
                    );
                    return;
                  }

                  setState(() {
                    final idx = slot.workouts.indexWhere((w) => w.id == workout.id);
                    if (idx != -1) {
                      slot.workouts[idx] = workout.copyWith(
                        name: nameCtrl.text,
                        durationMinutes: dur,
                        caloriesBurned: dur * 6,
                      );
                    }
                  });

